public class pb30 {
  public static void main(String[] args) {
    int temperature = 40;

    if (temperature < 0) {
      System.out.println("It's freezing!");
    } else if (temperature < 20) {
      System.out.println("It's cool.");
    } else {
      System.out.println("It's warm.");
    }
  }
}
